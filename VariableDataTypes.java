class VariableDataTypes{

	public static void main(String[] args){
		
	// Declaring variable..

	int valueOne=10;
	float floatValue=10.2f;
	double doubleValue=23.879;
	char charValue='N';
	String stringValue="Naveen";
	boolean status=true;

	// Printing a given variable values..

		System.out.println("INT VALUE "+valueOne);
		System.out.println("FLOAT VALUE "+floatValue);
		System.out.println("DOUBLE VALUE "+doubleValue);
		System.out.println("CHAR VALUE "+charValue);
		System.out.println("STRING VALUE "+stringValue);
		System.out.println("BOOLEAN STATUS "+status);
}
}